using System;

namespace P3Variables
{
    class Program
    {
        static void Main(string[] args)
        {
            int x;
            x = 5;

            int y;
            y = 7;

            int z;
            z = x + y;

            Console.WriteLine("x = " +x);
            Console.WriteLine("y = " +y);
            Console.WriteLine("x + y = z");
            Console.WriteLine("z = " +z);
        }
    }
}
